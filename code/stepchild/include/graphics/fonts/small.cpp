enum TextAlign:uint8_t{
  ALIGN_JOY_LEFT,
  ALIGN_CENTER,
  ALIGN_RIGHT
};

//converts text to char
void printSmall(int16_t x, int16_t y, String text, uint16_t c, TextAlign a){
  switch(a){
    case TextAlign::ALIGN_CENTER:
      x-=text.length()*2+countChar(text,' ');
    case TextAlign::ALIGN_JOY_LEFT:
      for(uint16_t letter = 0; letter<text.length(); letter++){
        if(x>=screenWidth)
          return;
        char character = text.charAt(letter);
        x+=printSmall(x,y,character,c);
      }
      return;
    case TextAlign::ALIGN_RIGHT:
      x+=text.length()*4-countChar(text,' ');
      for(uint16_t letter = text.length()-1; letter>=0; letter--){
        if(x>=screenWidth)
          continue;
        if(x<0)
          return;
        char character = text.charAt(letter);
        x-=printSmall(x,y,character,c);
      }
      break;
  }
}

void printSmall_centered(int x, int y, String text, uint16_t c){
  printSmall(x,y,text,c,TextAlign::ALIGN_CENTER);
}

void printSmall(int16_t x, int16_t y, String text, uint16_t c){
  printSmall(x,y,text,c,TextAlign::ALIGN_JOY_LEFT);
}

//just converts a float to a string
void printSmall(int16_t x1, int16_t y1, float a, uint16_t c){
  printSmall(x1,y1,stringify(a),c);
}

//this one prints out one word at a time, with bounds
void printSmall_overflow(int16_t x, int16_t y, int8_t margin, String text, uint16_t c){
  int xCoord = x;
  int yCoord = y;
  String word = "";
  int wordlength = 0;
  for(uint8_t letter = 0; letter<text.length(); letter++){
    String character = text.substring(letter,letter+1);
    if(character != " " && character != "-"){
      if(wordlength+xCoord>=screenWidth-margin){
        xCoord = x;
        yCoord += 7;
        // //Serial.println(word);
        if(yCoord>screenHeight)
          return;
      }
      word+=character;
      wordlength+=4;
    }
    else{
      word+=character;
      printSmall(xCoord, yCoord, word, c);
      word = "";
      xCoord+=wordlength+4;
      wordlength = 0;
    }
  }
  //print whatever's remaining
  printSmall(xCoord, yCoord, word, c);
}
